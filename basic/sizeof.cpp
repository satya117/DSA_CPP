/*find the sizeof datatype using sizeof operator*/
#include<iostream>
using namespace std;

int main(){
    int a;
    float f;
    double d;
    char ch;
    cout<<"Size of int: "<<sizeof(a)<<endl;
    cout<<"Size of float: "<<sizeof(f)<<endl;
    cout<<"Size of double: "<<sizeof(d)<<endl;
    cout<<"Size of char: "<<sizeof(ch)<<endl;
    return 0;
}